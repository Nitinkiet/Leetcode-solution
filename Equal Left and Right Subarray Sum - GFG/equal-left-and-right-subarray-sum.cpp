class Solution {
  public:
    int equalSum(int n, vector<int> &a) {
        vector<int>ans(n+1);
        ans[n-1] = a[n-1];
        for(int i=n-2;i>=0;i--){
            ans[i] = ans[i+1] + a[i];
        }
        int sum=0,ind=-1;
        for(int i=0;i<n;i++){
            sum += a[i];
            if(sum==ans[i]){
                ind=i+1;
                break;
        }
        }
        return ind;
    }
};


